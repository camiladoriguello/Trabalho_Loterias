package br.edu.univas.vo;

public class Jogo {

	private int numJogo;
	private String date;
	
	
	public int getNumJogo() {
		return numJogo;
	}
	public void setNumJogo(int numJogo) {
		this.numJogo = numJogo;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}

}
